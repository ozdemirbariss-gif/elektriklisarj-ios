import Foundation

enum AppLanguage: String, CaseIterable, Codable {
    case tr
    case en

    var displayCode: String {
        rawValue.uppercased()
    }

    init(code: String) {
        switch code.lowercased() {
        case "en":
            self = .en
        default:
            self = .tr
        }
    }
}

